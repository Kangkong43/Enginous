    private void factorialActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_factorialActionPerformed
        //Change the text  into a double value with Double.parseDouble
        double t = Double.parseDouble(input.getText());
        
        double fact = 1;
        
        // Iterate from i = 1 to i = t, incrementing i by 1 each time (i++)
        for(int i=1; i<=t ;i++){
        
            // Multiply the factorial by every i from 1 to t
            fact = fact * i;
        
        }
         //Set the text to blank
        input.setText("");
        //Set the text to input + the fact value
        input.setText(input.getText()+fact);
    }//GEN-LAST:event_factorialActionPerformed

