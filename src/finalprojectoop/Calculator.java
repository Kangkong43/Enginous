    private void equalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_equalsActionPerformed
        //Change the text  into a double value with Double.parseDouble
        num2 = Double.parseDouble(input.getText());
        
        if(opr == "+"){
            // add num1 and num2 if opr is +
            result = num1 + num2;
            //Change the value from double to string and set the text to result
            input.setText(Double.toString(result));
        }else if(opr == "-"){
            // subtract num1 and num2 if opr is -
            result = num1 - num2;
             //Change the value from double to string and set the text to result
            input.setText(Double.toString(result));
        }else if(opr == "*"){
            // multiply num1 and num2 if opr is *
            result = num1 * num2;
            //Change the value from double to string and set the text to result
            input.setText(Double.toString(result));
        }else if(opr == "/"){
            // divide num1 and num2 if opr is /
            result = num1 / num2;
            //Change the value from double to string and set the text to result
            input.setText(Double.toString(result));
        }else if(opr == "%"){
            // Get the modulo of num1 and num2 if opr is %
            result = num1 % num2;
            //Change the value from double to string and set the text to result
            input.setText(Double.toString(result));
        }else if(opr == "x^y"){
            //Get the power of num1 and num2 if opr is x^y(num1 is the base and num2 is the exponent)
            result = Math.pow(num1, num2);
            //Change the value from double to string and set the text to result
            input.setText(Double.toString(result));
}
        
       

   
  
    }//GEN-LAST:event_equalsActionPerformed

    private void backspaceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backspaceActionPerformed
        
        String backSpace = null;
        
        // Check if Input Field is Not Empty
        if(input.getText().length()>0){
        // StringBuilder allows the Manipulation of string(input.getText())
        StringBuilder s = new StringBuilder(input.getText());
        
         // Delete Last Character
        s.deleteCharAt(input.getText().length()-1);
        
        // Convert StringBuilder to String
        backSpace = s.toString();
        
        // Set Text in Input Field
        input.setText(backSpace);
        }
    }//GEN-LAST:event_backspaceActionPerformed


